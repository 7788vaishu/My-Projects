#use case of list
#list are typically used when you have a collection of items that may need to 
#be modified, such as adding or removing elements from the collection.

#simple example of list
l1=["Apple","Banana","Mango"]
print(l1)

#slicing
l2=[1,2,3,4,5,6]
print(l2[-5:-1])

#reverse
print(l2[::-1])

#index
l1=[1,2,3,4,5,6,7]
print(l1.index(4))

